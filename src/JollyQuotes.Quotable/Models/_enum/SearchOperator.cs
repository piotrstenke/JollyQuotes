namespace JollyQuotes.Quotable.Models
{
	/// <summary>
	/// Defines logical operators that can be used in a search query.
	/// </summary>
	public enum SearchOperator
	{
		/// <summary>
		/// No operator.
		/// </summary>
		None,

		/// <summary>
		/// Represents the AND operator.
		/// </summary>
		And,

		/// <summary>
		/// Represents the OR operator.
		/// </summary>
		Or
	}
}
